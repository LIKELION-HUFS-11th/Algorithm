import sys
input = sys.stdin.readline

n, k = map(int, input().split())
dp = [[0] * (k + 1) for _ in range(n + 1)]

for i in range(n + 1):
    for j in range(1, k + 1):
        if i == 0:  # [0, 1] [0, 2] 등 n이 0이면 경우의 수가 모두 1
            dp[i][j] = 1
        else:
            dp[i][j] = dp[i - 1][j] + dp[i][j - 1]

print(dp[n][k] % 1000000000)


